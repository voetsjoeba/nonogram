package com.voetsjoeba.nonogram.exception;

public class NoFurtherConsistencyInformationException extends RuntimeException {
	
	public NoFurtherConsistencyInformationException() {
		super();
	}
	
	public NoFurtherConsistencyInformationException(String message, Throwable cause) {
		super(message, cause);
	}
	
	public NoFurtherConsistencyInformationException(String message) {
		super(message);
	}
	
	public NoFurtherConsistencyInformationException(Throwable cause) {
		super(cause);
	}
	
}
